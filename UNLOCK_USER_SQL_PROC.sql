
USE ROLE SECURITYADMIN;
DROP PROCEDURE IF EXISTS PLATFORM_DB.PROVISION_ROUTINE.UNLOCK_USER_SQL_PROC (VARCHAR,INTEGER,VARCHAR);


USE ROLE SYSADMIN;

CREATE or REPLACE PROCEDURE  PLATFORM_DB.PROVISION_ROUTINE.UNLOCK_USER_SQL_PROC(
        P_USER_NAME VARCHAR,
        P_MINS_TO_UNLOCK INTEGER,
        P_TENANT_NAME VARCHAR
        )

RETURNS ARRAY NOT NULL
LANGUAGE SQL
EXECUTE AS   OWNER
AS
$$
DECLARE

    TENANT VARCHAR DEFAULT '';
    JOB_NAME VARCHAR DEFAULT '';
    JOB_ACTION VARCHAR DEFAULT '';
    IS_ERROR VARCHAR DEFAULT '0';
    JOB_LOG_DESCRIPTION VARCHAR DEFAULT '';
    TENANT_EXCEPTION_PARAMETER EXCEPTION;
    ROLE_NAME_EXCEPTION EXCEPTION;
    USER_SQL VARCHAR DEFAULT '';
    AR_PREFIX STRING DEFAULT '_';

    LOG VARIANT DEFAULT '';

BEGIN

    IS_ERROR := '0';
    TENANT := P_TENANT_NAME;
  
    USER_SQL :=  'ALTER USER ' || P_USER_NAME || ' SET MINS_TO_UNLOCK = ' || P_MINS_TO_UNLOCK || ' ;';
    EXECUTE IMMEDIATE USER_SQL;
    JOB_LOG_DESCRIPTION := '--' ||P_USER_NAME || ' ENABLED: '|| P_DISABLE_FLAG  || ' ALTER_SQL:'  || USER_SQL;

    RETURN ARRAY_CONSTRUCT(:IS_ERROR,:JOB_LOG_DESCRIPTION)  ;

EXCEPTION
  WHEN OTHER THEN
    IS_ERROR := '1';
    JOB_LOG_DESCRIPTION := 'SQLCODE:' || SQLCODE || ' SQLERRM:' || SQLERRM || ' SQLSTATE:' ||SQLSTATE;
    RETURN ARRAY_CONSTRUCT(:IS_ERROR,:JOB_LOG_DESCRIPTION)  ;

END;
$$;


grant ownership on procedure PLATFORM_DB.PROVISION_ROUTINE.UNLOCK_USER_SQL_PROC (VARCHAR,INTEGER,VARCHAR) to ROLE SECURITYADMIN;

USE ROLE SECURITYADMIN;
grant USAGE on procedure PLATFORM_DB.PROVISION_ROUTINE.UNLOCK_USER_SQL_PROC (VARCHAR,INTEGER,VARCHAR) to ROLE SYSADMIN;
